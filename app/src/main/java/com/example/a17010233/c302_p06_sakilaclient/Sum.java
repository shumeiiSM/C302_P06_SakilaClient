package com.example.a17010233.c302_p06_sakilaclient;

public class Sum {
    private String cat;
    private String num;

    public Sum(String cat, String num) {
        this.cat = cat;
        this.num = num;
    }

    public String getCat() {
        return cat;
    }

    public void setCat(String cat) {
        this.cat = cat;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }
}
